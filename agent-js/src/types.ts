import type {
  Message,
  ModelResponse,
  Part,
  PartialModelResponse,
  ToolResultStatus,
} from "@hoangvvo/llm-sdk";

export type AgentResponseStatus = "completed" | "cancelled";

export interface AgentRequest<TContext> {
  /**
   * The input items for this run, such as LLM messages.
   */
  input: AgentItem[];
  /**
   * The context used to resolve instructions and passed to tool executions.
   */
  context: TContext;
}

/**
 * A best-effort snapshot of work completed before an agent run failed.
 */
export interface AgentRunSnapshot {
  /**
   * The fully materialized items generated before the snapshot was created.
   */
  output: AgentItem[];
}

export interface AgentResponse extends AgentRunSnapshot {
  /**
   * The last assistant output content generated by the agent.
   */
  content: Part[];

  /**
   * The terminal status of the run.
   */
  status: AgentResponseStatus;
}

/**
 * An input for or output entry generated by the agent run
 */
export type AgentItem =
  AgentItemMessage | AgentItemModelResponse | AgentItemTool;

/**
 * A LLM message used in the run
 */
export type AgentItemMessage = { type: "message" } & Message;

/**
 * A model response generated in the run
 */
export interface AgentItemModelResponse extends ModelResponse {
  type: "model";
}

/**
 * A collection of tool call and result generated by the agent during a turn
 */
export interface AgentItemTool {
  type: "tool";
  /**
   * A unique ID for the tool call
   */
  tool_call_id: string;
  /**
   * The name of the tool called
   */
  tool_name: string;
  /**
   * The input provided to the tool
   */
  input: Record<string, unknown>;
  /**
   * The result of the tool call
   */
  output: Part[];
  /**
   * The terminal status of the tool call.
   */
  status: ToolResultStatus;
}

/**
 * Stream events emitted by the agent during runStream call.
 */
export type AgentStreamEvent =
  AgentStreamPartialEvent | AgentStreamItemEvent | AgentStreamResponseEvent;

export interface AgentStreamPartialEvent extends PartialModelResponse {
  event: "partial";
}

export interface AgentStreamItemEvent {
  event: "item";
  index: number;
  item: AgentItem;
}

export interface AgentStreamResponseEvent extends AgentResponse {
  event: "response";
}
